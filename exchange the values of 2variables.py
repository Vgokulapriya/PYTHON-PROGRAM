# exchange the values of 2variables
num1 = int(input("Enter the 1st number:"))
num2 = int(input("Enter the 2nd number:"))
print("value of 1st number before swapping:", num1)
print("value of 1st number before swapping:", num2)
temp = num1
num1 = num2
num2 = temp
print("value of 1st number after swapping:", num1)
print("value of 1st number after swapping:", num2)
